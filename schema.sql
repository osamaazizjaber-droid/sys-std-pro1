CREATE POLICY "Allow public all on students" ON public.students FOR ALL USING (true);
CREATE POLICY "Allow public all on student_history" ON public.student_history FOR ALL USING (true);
CREATE POLICY "Allow public all on professors" ON public.professors FOR ALL USING (true);
CREATE POLICY "Allow public all on attendance" ON public.attendance FOR ALL USING (true);
CREATE POLICY "Allow public all on settings" ON public.settings FOR ALL USING (true);

-- 6. Subject Assignments Table
CREATE TABLE IF NOT EXISTS public.subject_assignments (
    id bigint generated by default as identity primary key,
    stage_name text not null,
    subject_name text not null,
    prof_id text references public.professors(prof_id) on delete set null,
    unique(stage_name, subject_name)
);

ALTER TABLE public.subject_assignments ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow public all on subject_assignments" ON public.subject_assignments FOR ALL USING (true);
