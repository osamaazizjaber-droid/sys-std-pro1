-- Supabase PostgreSQL Schema for SYS STD Pro

-- 0. Companies (Colleges/Admins) Table
CREATE TABLE IF NOT EXISTS public.companies (
    id uuid references auth.users not null primary key,
    company_code text unique not null,
    company_name text not null,
    university_name text,
    department_name text,
    full_name text not null,
    position text,
    phone text,
    email text unique not null,
    status text default 'pending',
    created_at timestamp with time zone default timezone('utc'::text, now())
);

-- 1. Students Table
CREATE TABLE IF NOT EXISTS public.students (
    student_id text primary key, -- e.g. std00xx
    student_name text not null,
    grade text,
    academic_year text,
    photo_url text,
    reg_date timestamp with time zone default timezone('utc'::text, now()),
    status text default 'Active'
);

-- 2. Professors Table
CREATE TABLE IF NOT EXISTS public.professors (
    prof_id text primary key, -- e.g. prof001
    prof_name text not null,
    subject text,
    teaching_stage text,
    photo_url text,
    reg_date timestamp with time zone default timezone('utc'::text, now()),
    status text default 'Active'
);

-- 3. Attendance Table
CREATE TABLE IF NOT EXISTS public.attendance (
    id bigint generated by default as identity primary key,
    student_id text references public.students(student_id) on delete cascade,
    student_name text not null,
    prof_id text references public.professors(prof_id) on delete set null,
    prof_name text,
    date date not null,
    subject text,
    grade text,
    status text,
    check_in text,
    check_out text,
    notes text,
    unique(student_id, date, subject, prof_id)
);

-- 4. Student History Table
CREATE TABLE IF NOT EXISTS public.student_history (
    id bigint generated by default as identity primary key,
    student_id text references public.students(student_id) on delete cascade,
    student_name text not null,
    academic_year text not null,
    grade text not null,
    status text not null, -- 'Passed', 'Failed', 'Graduated'
    recorded_at timestamp with time zone default timezone('utc'::text, now())
);

-- 5. Settings Table
CREATE TABLE IF NOT EXISTS public.settings (
    id bigint primary key check (id = 1),
    language text default 'ar',
    theme text default 'light',
    font_family text default 'Arial',
    font_scale numeric default 1.0,
    accent_color text default '#F49000'
);

